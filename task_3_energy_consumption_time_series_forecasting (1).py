def _style_ax(ax):
    ax.set_facecolor("#1c1f26")
    ax.tick_params(colors="white", labelsize=8)
    for spine in ax.spines.values():
        spine.set_edgecolor("#3a3f4b")
    ax.yaxis.label.set_color("white")
    ax.xaxis.label.set_color("white")
    plt.setp(ax.get_xticklabels(), rotation=30, ha="right")

# SECTION 8 ─ FEATURE IMPORTANCE (XGBoost)

def plot_feature_importance(model, top_n: int = 12):
    fi = (
        pd.Series(model.get_booster().get_fscore())
        .sort_values(ascending=True)
        .tail(top_n)
    )
    fig, ax = plt.subplots(figsize=(9, 5))
    fig.patch.set_facecolor("#0f1117")
    bars = ax.barh(fi.index, fi.values, color="#f39c12", alpha=0.85)
    ax.set_title(f"XGBoost – Top {top_n} Feature Importances",
                 color="white", fontsize=12, pad=10)
    _style_ax(ax)
    plt.tight_layout()
    plt.savefig("xgboost_feature_importance.png", dpi=150, bbox_inches="tight",
                facecolor=fig.get_facecolor())
    plt.show()
    print("✔  Feature importance plot saved → xgboost_feature_importance.png")

# MAIN

def main():
    print("\n" + "═" * 60)
    print("  ENERGY CONSUMPTION FORECASTING PIPELINE")
    print("═" * 60)

    # 1. Load
    print("\n[1/5] Loading data …")
    raw     = load_data("household_power_consumption.txt")

    # 2. Resample & feature engineering
    print("[2/5] Resampling to hourly & engineering features …")
    hourly  = resample_hourly(raw)
    featured = engineer_features(hourly)
    print(f"      Hourly rows: {len(featured):,}  |  Features: {len(FEATURE_COLS)}")

    # 3. Split
    TEST_HOURS = 168  # 7 days
    train, test = train_test_split(featured, test_hours=TEST_HOURS)
    print(f"      Train: {len(train):,} h  |  Test: {len(test):,} h (7 days)")

    # 4. Models
    print("\n[3/5] Training models …")
    arima_preds          = run_arima(train, test)
    prophet_preds        = run_prophet(train, test)
    xgboost_preds, xg_m  = run_xgboost(train, test)

    # 5. Evaluate
    print("\n[4/5] Evaluation metrics …")
    actual  = test["power"].values
    metrics = [
        evaluate(actual, arima_preds,   "ARIMA"),
        evaluate(actual, prophet_preds, "Prophet"),
        evaluate(actual, xgboost_preds, "XGBoost"),
    ]

    # 6. Visualise
    print("\n[5/5] Plotting results …")
    preds = {
        "ARIMA":   arima_preds,
        "Prophet": prophet_preds,
        "XGBoost": xgboost_preds,
    }
    plot_results(test, preds, metrics, train)
    plot_feature_importance(xg_m)

    # 7. Summary table
    print("\n" + "═" * 60)
    print("  FINAL LEADERBOARD")
    print("═" * 60)
    df_m = pd.DataFrame(metrics).set_index("model").sort_values("RMSE")
    print(df_m.to_string(float_format="{:.4f}".format))
    best = df_m.index[0]
    print(f"\n  🏆  Best model by RMSE: {best}")
    print("═" * 60 + "\n")


if __name__ == "__main__":
    main()