# SECTION 1 ─ LOAD & PARSE DATA

def load_data(filepath: str = "household_power_consumption.txt") -> pd.DataFrame:
    """
    Load the UCI Household Power Consumption dataset.
    Falls back to synthetic data if the file is not found.
    """
    try:
        df = pd.read_csv(
            filepath,
            sep=";",
            parse_dates={"datetime": ["Date", "Time"]},
            dayfirst=True,
            na_values=["?"],
            low_memory=False,
        )
        df.set_index("datetime", inplace=True)
        df = df[["Global_active_power"]].dropna()
        df.columns = ["power"]
        df["power"] = pd.to_numeric(df["power"])
        print(f"✔  Loaded real dataset: {len(df):,} rows  ({df.index.min()} → {df.index.max()})")
        return df

    except FileNotFoundError:
        print("⚠  Dataset file not found – generating synthetic data for demonstration.\n"
              "   Download the real dataset from:\n"
              "   https://archive.ics.uci.edu/ml/datasets/individual+household+electric+power+consumption\n"
              "   and place 'household_power_consumption.txt' in the working directory.")
        return _make_synthetic()


def _make_synthetic(days: int = 90) -> pd.DataFrame:
    """Synthetic 1-minute household power with realistic daily/weekly cycles."""
    rng = pd.date_range("2023-01-01", periods=days * 24 * 60, freq="1min")
    hour   = rng.hour + rng.minute / 60
    dow    = rng.dayofweek

    daily   = 1.2 * np.sin(2 * np.pi * (hour - 6) / 24) + 1.5
    weekend = np.where(dow >= 5, 0.3, 0.0)
    noise   = np.random.normal(0, 0.15, len(rng))
    power   = np.clip(daily + weekend + noise, 0.05, None)

    return pd.DataFrame({"power": power}, index=rng)

# SECTION 2 ─ RESAMPLE & FEATURE ENGINEERING

def resample_hourly(df: pd.DataFrame) -> pd.DataFrame:
    """Resample minute-level data to hourly means."""
    hourly = df["power"].resample("1h").mean().dropna()
    return hourly.to_frame(name="power")


def engineer_features(df: pd.DataFrame) -> pd.DataFrame:
    """Add time-based features for ML model."""
    out = df.copy()
    out["hour"]       = out.index.hour
    out["dayofweek"]  = out.index.dayofweek
    out["month"]      = out.index.month
    out["is_weekend"] = (out.index.dayofweek >= 5).astype(int)
    out["hour_sin"]   = np.sin(2 * np.pi * out["hour"] / 24)
    out["hour_cos"]   = np.cos(2 * np.pi * out["hour"] / 24)
    out["dow_sin"]    = np.sin(2 * np.pi * out["dayofweek"] / 7)
    out["dow_cos"]    = np.cos(2 * np.pi * out["dayofweek"] / 7)

    # Lag features
    for lag in [1, 2, 3, 24, 48]:
        out[f"lag_{lag}"] = out["power"].shift(lag)

    # Rolling statistics
    out["rolling_mean_6h"]  = out["power"].shift(1).rolling(6).mean()
    out["rolling_mean_24h"] = out["power"].shift(1).rolling(24).mean()
    out["rolling_std_24h"]  = out["power"].shift(1).rolling(24).std()

    return out.dropna()


def train_test_split(df: pd.DataFrame, test_hours: int = 168):
    """Last `test_hours` hours as test set (default = 7 days)."""
    train = df.iloc[:-test_hours]
    test  = df.iloc[-test_hours:]
    return train, test

# SECTION 3 ─ MODEL: ARIMA
def run_arima(train: pd.DataFrame, test: pd.DataFrame) -> np.ndarray:
    """Fit ARIMA(2,1,2) on training data; forecast over test horizon."""
    print("  Fitting ARIMA …", end=" ", flush=True)
    model = ARIMA(train["power"], order=(2, 1, 2))
    fit   = model.fit()
    fc    = fit.forecast(steps=len(test))
    preds = np.clip(np.array(fc), 0, None)
    print("done")
    return preds

# SECTION 4 ─ MODEL: PROPHET

def run_prophet(train: pd.DataFrame, test: pd.DataFrame) -> np.ndarray:
    """Fit Facebook/Meta Prophet with daily + weekly seasonality."""
    print("  Fitting Prophet …", end=" ", flush=True)

    prophet_df = train[["power"]].reset_index()
    prophet_df.columns = ["ds", "y"]

    m = Prophet(
        yearly_seasonality=False,
        weekly_seasonality=True,
        daily_seasonality=True,
        changepoint_prior_scale=0.1,
    )
    m.fit(prophet_df)

    future = pd.DataFrame({"ds": test.index})
    fc     = m.predict(future)
    preds  = np.clip(fc["yhat"].values, 0, None)
    print("done")
    return preds

# SECTION 5 ─ MODEL: XGBOOST

FEATURE_COLS = [
    "hour", "dayofweek", "month", "is_weekend",
    "hour_sin", "hour_cos", "dow_sin", "dow_cos",
    "lag_1", "lag_2", "lag_3", "lag_24", "lag_48",
    "rolling_mean_6h", "rolling_mean_24h", "rolling_std_24h",
]


def run_xgboost(train: pd.DataFrame, test: pd.DataFrame) -> np.ndarray:
    """Train XGBoost regressor on engineered features."""
    print("  Fitting XGBoost …", end=" ", flush=True)

    X_train = train[FEATURE_COLS]
    y_train = train["power"]
    X_test  = test[FEATURE_COLS]

    model = xgb.XGBRegressor(
        n_estimators=300,
        learning_rate=0.05,
        max_depth=5,
        subsample=0.8,
        colsample_bytree=0.8,
        random_state=42,
        verbosity=0,
    )
    model.fit(X_train, y_train, eval_set=[(X_test, test["power"])], verbose=False)
    preds = np.clip(model.predict(X_test), 0, None)
    print("done")
    return preds, model

# SECTION 6 ─ EVALUATION

def evaluate(actual: np.ndarray, predicted: np.ndarray, name: str) -> dict:
    mae  = mean_absolute_error(actual, predicted)
    rmse = np.sqrt(mean_squared_error(actual, predicted))
    mape = np.mean(np.abs((actual - predicted) / (actual + 1e-8))) * 100
    print(f"    {name:<10}  MAE={mae:.4f}  RMSE={rmse:.4f}  MAPE={mape:.2f}%")
    return {"model": name, "MAE": mae, "RMSE": rmse, "MAPE": mape}

