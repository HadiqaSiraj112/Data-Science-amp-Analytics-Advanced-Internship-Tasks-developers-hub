def lime_explain(model, instance, feature_names,
                 n_samples=700, noise_std=0.18, top_k=8,
                 scaler_obj=None):
    """
    LIME — Local Interpretable Model-agnostic Explanations.

    Algorithm
    ---------
    1. Perturb the instance by adding Gaussian noise (n_samples times).
    2. Weight each perturbed sample by Gaussian kernel proximity.
    3. Collect model probability predictions on perturbed samples.
    4. Fit a weighted linear regression (interpretable surrogate).
    5. Return top-k coefficients as local feature importances.
    """
    x   = np.array(instance, dtype=float).reshape(1, -1)
    rng = np.random.RandomState(0)

    noise   = rng.randn(n_samples, len(feature_names)) * noise_std
    samples = x + noise

    dists   = np.linalg.norm(noise, axis=1)
    sigma   = np.percentile(dists, 75) + 1e-8
    weights = np.exp(-(dists**2) / (2 * sigma**2))

    in_model = scaler_obj.transform(samples) if scaler_obj else samples
    preds    = model.predict_proba(in_model)[:, 1]

    W    = np.diag(weights)
    XtW  = samples.T @ W
    coef = np.linalg.lstsq(XtW @ samples, XtW @ preds, rcond=None)[0]

    pairs = sorted(zip(feature_names, coef),
                   key=lambda kv: abs(kv[1]), reverse=True)
    return pairs[:top_k]


